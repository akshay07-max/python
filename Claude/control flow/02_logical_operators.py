# Welcome! Now you know how to check single conditions. But what if you need to check multiple conditions at once? That's where logical operators come in!

# Part 1: What are Logical Operators?
# Real-world example:
# You can enter a club IF:

# You are 21+ years old AND you have a valid ID

# You get a discount IF:

# You are a student OR you are a senior citizen

# You cannot enter IF:

# You do NOT have a ticket

# In programming, logical operators combine conditions:

# and - Both conditions must be True
# or - At least one condition must be True
# not - Reverses the boolean value